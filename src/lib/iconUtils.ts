// Cache for loaded images
const imageCache = new Map<string, HTMLImageElement>();

/**
 * Creates an SVG data URL for an icon with the specified color
 */
export function createIconDataUrl(iconName: string, color: string = '#ffffff', size: number = 48): string {
  const pathData = ICON_PATHS[iconName];
  if (!pathData) return '';

  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${encodeURIComponent(color)}" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${pathData}</svg>`;

  return `data:image/svg+xml,${encodeURIComponent(svg)}`;
}

/**
 * Loads an icon as an HTMLImageElement (required by spin-wheel library)
 * Returns a promise that resolves to the loaded image
 */
export function loadIconImage(iconName: string, color: string = '#ffffff', size: number = 48): Promise<HTMLImageElement> {
  const cacheKey = `${iconName}-${color}-${size}`;

  // Return cached image if available
  if (imageCache.has(cacheKey)) {
    return Promise.resolve(imageCache.get(cacheKey)!);
  }

  return new Promise((resolve, reject) => {
    const dataUrl = createIconDataUrl(iconName, color, size);
    if (!dataUrl) {
      reject(new Error(`Unknown icon: ${iconName}`));
      return;
    }

    const img = new Image();
    img.onload = () => {
      imageCache.set(cacheKey, img);
      resolve(img);
    };
    img.onerror = () => reject(new Error(`Failed to load icon: ${iconName}`));
    img.src = dataUrl;
  });
}

/**
 * Preloads multiple icons and returns them as a map
 */
export async function preloadIcons(
  icons: Array<{ name: string; color?: string; size?: number }>
): Promise<Map<string, HTMLImageElement>> {
  const results = new Map<string, HTMLImageElement>();

  await Promise.all(
    icons.map(async ({ name, color = '#ffffff', size = 48 }) => {
      try {
        const img = await loadIconImage(name, color, size);
        results.set(name, img);
      } catch (e) {
        console.warn(`Failed to preload icon ${name}:`, e);
      }
    })
  );

  return results;
}

/**
 * Check if an icon name is valid
 */
export function isValidIcon(iconName: string): boolean {
  return iconName in ICON_PATHS;
}
