interface IconPickerProps {
  value?: string;
  onChange: (iconName: string | undefined) => void;
}

export function IconPicker({ value, onChange }: IconPickerProps) {
  const [isExpanded, setIsExpanded] = useState(false);

  const iconEntries = Object.entries(AVAILABLE_ICONS);

  return (
    <div className="space-y-2">
      <Label>Icon (optional)</Label>
      <div className="flex flex-wrap gap-2">
        {/* No icon option */}
        <Button
          type="button"
          variant={!value ? 'default' : 'outline'}
          size="sm"
          onClick={() => onChange(undefined)}
          className="h-10 w-10 p-0"
          title="No icon"
        >
          <span className="text-xs">-</span>
        </Button>

        {/* Show first row of icons or all if expanded */}
        {iconEntries.slice(0, isExpanded ? iconEntries.length : 7).map(([name, Icon]) => (
          <Button
            key={name}
            type="button"
            variant={value === name ? 'default' : 'outline'}
            size="sm"
            onClick={() => onChange(name)}
            className={cn('h-10 w-10 p-0')}
            title={name}
          >
            <Icon className="h-5 w-5" />
          </Button>
        ))}

        {/* Expand/collapse button */}
        <Button
          type="button"
          variant="ghost"
          size="sm"
          onClick={() => setIsExpanded(!isExpanded)}
          className="h-10 px-3"
        >
          {isExpanded ? 'Less' : `+${iconEntries.length - 7} more`}
        </Button>
      </div>
    </div>
  );
}

// Helper to get SVG string for an icon
export function getIconSvgString(iconName: string, color: string = '#ffffff'): string {
  const Icon = AVAILABLE_ICONS[iconName];
  if (!Icon) return '';

  // Create SVG string with the icon path
  // We need to render the icon to get its path data
  return `<svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${getIconPath(iconName)}</svg>`;
}

